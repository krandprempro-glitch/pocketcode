class ClaudeCodeCommandManager private constructor() {

    companion object {
        private const val LOG_TAG = "ClaudeCodeCommandManager"
        // 远程索引文件路径: ~/.claude/commands/commands.md
        // 由 /cmd-index skill 生成
        private const val COMMANDS_INDEX_FILE = "/.claude/commands/commands.md"

        @Volatile
        private var instance: ClaudeCodeCommandManager? = null

        @JvmStatic
        fun getInstance(): ClaudeCodeCommandManager {
            return instance ?: synchronized(this) {
                instance ?: ClaudeCodeCommandManager().also { instance = it }
            }
        }
    }

    @Volatile
    private var cachedRemoteCommands: List<ClaudeCodeMenuHelper.Command> = emptyList()

    /** BehaviorSubject that emits the last cached value to new subscribers. */
    private val customCommandsSubject = BehaviorSubject.createDefault<List<ClaudeCodeMenuHelper.Command>>(emptyList())

    /** Observable of custom command list changes. New subscribers receive the last emitted value. */
    @JvmField var customCommandsObservable = customCommandsSubject.hide()

