    /**
     * Fetches custom commands by reading ~/.claude/commands/commands.md via SFTP.
     *
     * The commands.md file is generated by the /cmd-index Claude Code skill:
     * Format:
     *   ## /user:filename
     *   description line
     *
     * Returns empty list if:
     * - SFTP not connected
     * - commands.md file does not exist
     * - parsing fails
     */
    fun fetchRemoteCommands(forceRefresh: Boolean = false): Single<List<ClaudeCodeMenuHelper.Command>> {
        if (!forceRefresh && cachedRemoteCommands.isNotEmpty()) {
            customCommandsSubject.onNext(cachedRemoteCommands)
            return Single.just(cachedRemoteCommands)
        }

        val sftpManager = SFTPConnectionManager.getInstance()
        val connected = sftpManager.isConnected
        Logger.logDebug(LOG_TAG, "fetchRemoteCommands: isConnected=$connected, homeDir=${sftpManager.currentWorkingDirectory}")

        if (!connected) {
            Logger.logInfo(LOG_TAG, "SFTP not connected, returning empty custom commands")
            val empty = emptyList<ClaudeCodeMenuHelper.Command>()
            customCommandsSubject.onNext(empty)
            return Single.just(empty)
        }

        val homeDir = sftpManager.currentWorkingDirectory
        val indexFile = homeDir.trimEnd('/') + COMMANDS_INDEX_FILE
        Logger.logDebug(LOG_TAG, "Reading commands index from: $indexFile")

        return sftpManager.readFileContent(indexFile)
            .map { content ->
                Logger.logDebug(LOG_TAG, "readFileContent success, content length=${content.length}, first 200 chars: ${content.take(200)}")
                val parsed = parseCommandsIndex(content)
                Logger.logDebug(LOG_TAG, "parseCommandsIndex returned ${parsed.size} commands: ${parsed.map { it.command + "->" + it.description }}")
                parsed
            }
            .subscribeOn(Schedulers.io())
            .observeOn(AndroidSchedulers.mainThread())
            .doOnSuccess { commands ->
                cachedRemoteCommands = commands
                customCommandsSubject.onNext(commands)
                Logger.logInfo(LOG_TAG, "Fetched ${commands.size} custom commands from commands.md")
            }
            .onErrorReturn { e ->
                Logger.logInfo(LOG_TAG, "No commands.md found or read error: ${e.message}. Run /cmd-index on remote to generate it.")
                val empty = emptyList<ClaudeCodeMenuHelper.Command>()
                cachedRemoteCommands = empty
                customCommandsSubject.onNext(empty)
                empty
            }
    }

    /**
     * Parse commands.md index file.
     * Format:
     *   ## /user:filename  or  ## /filename
     *   description line
     *
     * Supports both /user:prefixed commands and plain /commands.
     * The description is the first non-empty line after the ## line.
     */
    private fun parseCommandsIndex(content: String): List<ClaudeCodeMenuHelper.Command> {
        val lines = content.lineSequence().toList()
        val commands = mutableListOf<ClaudeCodeMenuHelper.Command>()

        var i = 0
        while (i < lines.size) {
            val line = lines[i].trim()
            // Match ## /user:filename or ## /filename (but not ## /user: which is incomplete)
            val commandName = when {
                line.startsWith("## /user:") -> line.removePrefix("## /user:").trim()
                line.startsWith("## /") && !line.startsWith("## /user:") -> line.removePrefix("## /").trim()
                else -> null
            }

            if (commandName != null) {
                val slashCommand = "/$commandName"

                // Next non-empty line is the description
                var description = commandName
                for (j in (i + 1) until lines.size) {
                    val descLine = lines[j].trim()
                    if (descLine.isNotEmpty() && !descLine.startsWith("#") && !descLine.startsWith("##")) {
                        description = descLine
                        break
                    }
                    if (descLine.startsWith("##")) break
                }

                Logger.logDebug(LOG_TAG, "Parsed custom command: $slashCommand -> $description")
                commands.add(ClaudeCodeMenuHelper.Command(slashCommand, description))
            }
            i++
        }

        return commands
    }

    fun getCustomCommands(): List<ClaudeCodeMenuHelper.Command> {
        return cachedRemoteCommands
    }

    fun refreshRemoteCommands(): Single<List<ClaudeCodeMenuHelper.Command>> {
        clearCache()
        return fetchRemoteCommands(forceRefresh = true)
    }

    fun clearCache() {
        cachedRemoteCommands = emptyList()
        customCommandsSubject.onNext(emptyList())
        Logger.logInfo(LOG_TAG, "Custom command cache cleared")
    }
}
